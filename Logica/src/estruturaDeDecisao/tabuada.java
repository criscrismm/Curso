package estruturaDeDecisao;

public class tabuada {

    public static void main(String[] args) {
        
        int cont = 1;
        int tabuada;
        while (cont<11) {
            tabuada = 1*(cont);
            System.out.println("1x" +cont + "=" +tabuada);
            cont = cont +1;
        }

    }

}
